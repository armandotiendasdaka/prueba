-- Estructura para MySQL/PostgreSQL (Ajustado desde SQL Server)
-- Crear Esquema/Base de Datos
-- PostgreSQL: CREATE SCHEMA FEBECA;
-- MySQL: CREATE DATABASE SOFTLANDPRD; USE SOFTLANDPRD;

CREATE TABLE FEBECA.VENDEDOR (
    VENDEDOR VARCHAR(10) PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    ACTIVO CHAR(1) DEFAULT 'S'
);
    
CREATE TABLE FEBECA.CLIENTE (
    CLIENTE VARCHAR(20) PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    DIRECCION TEXT,
    SALDO DECIMAL(28, 8),
    ZONA VARCHAR(20)
);

CREATE TABLE FEBECA.CLIENTE_VENDEDOR (
    CLIENTE VARCHAR(20),
    VENDEDOR VARCHAR(10),
    PRIMARY KEY (CLIENTE, VENDEDOR),
    FOREIGN KEY (CLIENTE) REFERENCES FEBECA.CLIENTE(CLIENTE),
    FOREIGN KEY (VENDEDOR) REFERENCES FEBECA.VENDEDOR(VENDEDOR)
);

CREATE TABLE FEBECA.ARTICULO (
    ARTICULO VARCHAR(20) PRIMARY KEY,
    DESCRIPCION VARCHAR(254) NOT NULL,
    PRECIO_BASE_LOCAL DECIMAL(28, 8),
    ACTIVO CHAR(1) DEFAULT 'S'
);

CREATE TABLE FEBECA.EXISTENCIA_BODEGA (
    ARTICULO VARCHAR(20),
    BODEGA VARCHAR(10) NOT NULL,
    CANT_DISPONIBLE DECIMAL(28, 8) NOT NULL,
    PRIMARY KEY (ARTICULO, BODEGA),
    FOREIGN KEY (ARTICULO) REFERENCES FEBECA.ARTICULO(ARTICULO)
);